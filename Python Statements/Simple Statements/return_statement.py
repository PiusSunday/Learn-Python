## RETURN STATEMENT

# The return statement is used to exit a function early.

# When We create a function in Python to perform a specific task. The function can return a value that is nothing but an output of function execution.

# Using a return statement, we can return a value from a function when called.

# example:

# Define a function
# function acceptts two numbers and return their sum
def addition(num1, num2):
    return num1 + num2  # return the sum of two numbers


# result is the return value
result = addition(10, 20)
print(result)

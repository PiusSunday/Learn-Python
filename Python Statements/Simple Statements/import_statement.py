## IMPORT STATEMENT

# The import statement is used to import modules into a program. It can also be used to import individual functions, classes, and variables from a module.

# Python has many built-in modules that you can use without having to install them. For example, e can use the built-in DateTime module to get the current date and time.

# example:

import datetime

# get current datetime
now = datetime.datetime.now()
print(now)

## COMPOUND STATEMENTS

# Compound statements contain (groups of) other statements; they affect or control the execution of those other statements in some way.

# The compound statement includes the conditional and loop statement.

## TYPES OF COMPOUND STATEMENTS

# if statement: It is a control flow statement that will execute statements under it if the condition is true. Also kown as a conditional statement.

# while statement: It is a control flow statement that will execute statements under it until the condition is false. Also known as a looping statement.

# for statement: It is a control flow statement that will execute statements under it for a given number of times. Also known as a looping statement. The sequence can be string, list, dictionary, set, or tuple. Also known as a looping statement.


# try statement: specifies exception handlers. It is a control flow statement that will execute statements under it if an exception is raised.

# with statement: Used to cleanup code for a group of statements, while the with statement allows the execution of initialization and finalization code around a block of code.

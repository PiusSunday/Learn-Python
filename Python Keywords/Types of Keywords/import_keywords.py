''' Import Keywords: 'import', 'from', 'as' '''


from datetime import datetime
'# 1. import keyword: the import statement is used to import the whole module.'

# example:

import datetime

# get current datetime
now = datetime.datetime.now()
print(now)



'# 2. from keyword: the from statement is used to import specific functions and clases from a module.'

# example: (from keyword)

# import only datetime class

# get current datetime
now = datetime.now()
print(now)


# 3. as keyword: the as keyword is used to give a name to a module or a function imported from a module.

# example: (as keyword)

import pandas as pd
